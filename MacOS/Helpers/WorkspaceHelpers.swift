// MARK: - Screenshot and prompt helpers

private let attachedScreenshotRegex = try? NSRegularExpression(pattern: "\\[Screenshot attached:\\s*([^\\]]+)\\]")
private let attachedScreenshotDisplayRegex = try? NSRegularExpression(pattern: "\\s*\\[Screenshot attached:\\s*[^\\]]+\\]\\s*")

/// Returns the prompt with all "[Screenshot attached: path]" references removed, trimmed. Use for display only.
func userPromptDisplayText(from prompt: String) -> String {
    guard let regex = attachedScreenshotDisplayRegex else { return prompt }
    let range = NSRange(prompt.startIndex..., in: prompt)
    let stripped = regex.stringByReplacingMatches(
        in: prompt,
        range: range,
        withTemplate: "\n"
    )
    return stripped
        .components(separatedBy: .newlines)
        .map { $0.trimmingCharacters(in: .whitespacesAndNewlines) }
        .joined(separator: "\n")
        .trimmingCharacters(in: .whitespacesAndNewlines)
}

/// Extracts screenshot paths from prompt (e.g. ".metro/pasted-screenshot-1.png"), capped at maxScreenshots.
func screenshotPaths(from prompt: String) -> [String] {
    guard let regex = attachedScreenshotRegex else { return [] }
    let range = NSRange(prompt.startIndex..., in: prompt)
    let matches = regex.matches(in: prompt, range: range)
    var paths: [String] = []
    for m in matches {
        if m.numberOfRanges > 1, let r = Range(m.range(at: 1), in: prompt) {
            let path = String(prompt[r]).trimmingCharacters(in: .whitespaces)
            if !path.isEmpty, !paths.contains(path) {
                paths.append(path)
            }
        }
    }
    return Array(paths.prefix(AppLimits.maxScreenshots))
}

func estimatedContextTokens(prompt: String, conversationCharacterCount: Int) -> (used: Int, limit: Int) {
    let chars = prompt.count + conversationCharacterCount
    let used = max(0, chars / 4)
    return (used, AppLimits.contextTokenLimit)
}

func autoGeneratedTabTitle(from prompt: String) -> String? {
    let firstContentLine = prompt
        .components(separatedBy: .newlines)
        .map { $0.trimmingCharacters(in: .whitespacesAndNewlines) }
        .first { !$0.isEmpty && !$0.hasPrefix("[Screenshot attached:") }

    if let firstContentLine {
        let normalized = firstContentLine.replacingOccurrences(
            of: #"\s+"#,
            with: " ",
            options: .regularExpression
        )
        let truncated = String(normalized.prefix(72)).trimmingCharacters(in: .whitespacesAndNewlines)
        if !truncated.isEmpty { return truncated }
    }

    // Prompt is empty or only screenshot(s) — use a fallback so the tab doesn't stay "Agent N"
    if !screenshotPaths(from: prompt).isEmpty {
        return "Screenshot"
    }

    return nil
}

/// Picks the next available screenshot filename not in current paths (legacy workspace-relative paths).
func nextScreenshotPath(currentPaths: [String]) -> String? {
    let legacy = ".metro/pasted-screenshot.png"
    if !currentPaths.contains(legacy) { return legacy }
    for i in 1...AppLimits.maxScreenshots {
        let candidate = ".metro/pasted-screenshot-\(i).png"
        if !currentPaths.contains(candidate) { return candidate }
    }
    return nil
}

// MARK: - Screenshot cache (tmp in app support, same location as preferences/tab state)

/// Directory where pasted screenshots are stored: ~/Library/Application Support/CursorPlus/tmp. Creates the directory if needed.
func screenshotCacheDirectoryURL() -> URL {
    let base = FileManager.default.urls(for: .applicationSupportDirectory, in: .userDomainMask).first!
    let dir = base.appendingPathComponent("CursorPlus", isDirectory: true).appendingPathComponent("tmp", isDirectory: true)
    try? FileManager.default.createDirectory(at: dir, withIntermediateDirectories: true)
    return dir
}

/// Returns a unique full path for a new screenshot in the cache (for use in [Screenshot attached: path]).
func nextScreenshotCachePath() -> String {
    screenshotCacheDirectoryURL()
        .appendingPathComponent("\(UUID().uuidString).png")
        .path
}

/// Resolves a screenshot path from a prompt to a file URL. Path may be absolute (cache) or workspace-relative (legacy).
/// Use this when loading or deleting; missing files are handled by callers (e.g. NSImage(contentsOf:) returns nil).
func screenshotFileURL(path: String, workspacePath: String) -> URL {
    let trimmed = path.trimmingCharacters(in: .whitespaces)
    if trimmed.hasPrefix("/") {
        return URL(fileURLWithPath: trimmed)
    }
    return URL(fileURLWithPath: workspacePath).appendingPathComponent(trimmed)
}

/// Deletes screenshot cache files older than the given number of days. Call on app quit.
func deleteScreenshotCacheOlderThan(days: Int) {
    let dir = screenshotCacheDirectoryURL()
    let cutoff = Date().addingTimeInterval(-Double(days * 24 * 60 * 60))
    guard let contents = try? FileManager.default.contentsOfDirectory(
        at: dir,
        includingPropertiesForKeys: [.contentModificationDateKey],
        options: [.skipsHiddenFiles]
    ) else { return }
    for url in contents where url.pathExtension.lowercased() == "png" {
        guard let date = (try? url.resourceValues(forKeys: [.contentModificationDateKey]))?.contentModificationDate else { continue }
        if date < cutoff {
            try? FileManager.default.removeItem(at: url)
        }
    }
}

// MARK: - Project root (terminal / process working directory)

/// Returns the project root for running terminal or process. If `workspacePath` ends with `/.metro`, returns the parent directory so the shell/agent runs in the project root, not inside `.metro`.
func projectRootForTerminal(workspacePath: String) -> String {
    let trimmed = workspacePath.trimmingCharacters(in: .whitespacesAndNewlines)
    guard !trimmed.isEmpty else { return trimmed }
    let expanded = (trimmed as NSString).expandingTildeInPath
    let url = URL(fileURLWithPath: expanded)
    if url.lastPathComponent == ".metro" {
        return url.deletingLastPathComponent().path
    }
    return expanded
}

// MARK: - Project folders and Git

/// Returns immediate subdirectories of `rootPath` that contain a `.metro` directory (Metro projects only).
func loadDevFolders(rootPath: String) -> [URL] {
    let resolvedRootPath = AppPreferences.resolvedProjectsRootPath(rootPath)
    let url = URL(fileURLWithPath: resolvedRootPath)
    guard let contents = try? FileManager.default.contentsOfDirectory(
        at: url,
        includingPropertiesForKeys: [.isDirectoryKey],
        options: [.skipsHiddenFiles]
    ) else { return [] }
    let fm = FileManager.default
    return contents
        .filter { candidate in
            guard (try? candidate.resourceValues(forKeys: [.isDirectoryKey]).isDirectory) == true else { return false }
            let metroDir = candidate.appendingPathComponent(".metro", isDirectory: true)
            var isDir: ObjCBool = false
            return fm.fileExists(atPath: metroDir.path, isDirectory: &isDir) && isDir.boolValue
        }
        .sorted { $0.lastPathComponent.localizedStandardCompare($1.lastPathComponent) == .orderedAscending }
}

/// Ensures every immediate subdirectory of `rootPath` has a `.metro` directory so Metro can discover it.
/// Returns the number of `.metro` directories created during this pass.
@discardableResult
func seedMetroDirectories(rootPath: String) -> Int {
    let resolvedRootPath = AppPreferences.resolvedProjectsRootPath(rootPath)
    let rootURL = URL(fileURLWithPath: resolvedRootPath)
    guard let contents = try? FileManager.default.contentsOfDirectory(
        at: rootURL,
        includingPropertiesForKeys: [.isDirectoryKey],
        options: [.skipsHiddenFiles]
    ) else { return 0 }

    let fm = FileManager.default
    var createdCount = 0
    for candidate in contents {
        guard (try? candidate.resourceValues(forKeys: [.isDirectoryKey]).isDirectory) == true else { continue }

        let metroDir = candidate.appendingPathComponent(".metro", isDirectory: true)
        var isDir: ObjCBool = false
        if fm.fileExists(atPath: metroDir.path, isDirectory: &isDir), isDir.boolValue {
            continue
        }

        do {
            try fm.createDirectory(at: metroDir, withIntermediateDirectories: true)
            createdCount += 1
        } catch {
            continue
        }
    }

    return createdCount
}

/// Seeds `.metro` directories across multiple scan roots, deduplicated by normalized path.
/// Returns the total number of `.metro` directories created.
@discardableResult
func seedMetroDirectories(rootPaths: [String]) -> Int {
    var seen = Set<String>()
    return rootPaths.reduce(into: 0) { total, path in
        let normalized = (path as NSString).standardizingPath
        guard !normalized.isEmpty, seen.insert(normalized).inserted else { return }
        total += seedMetroDirectories(rootPath: normalized)
    }
}

/// Returns Metro projects discovered under any of the configured scan roots, deduplicated by path.
func loadDevFolders(rootPaths: [String]) -> [URL] {
    var seen = Set<String>()
    return rootPaths
        .flatMap(loadDevFolders(rootPath:))
        .filter { seen.insert($0.path).inserted }
        .sorted { $0.lastPathComponent.localizedStandardCompare($1.lastPathComponent) == .orderedAscending }
}

/// Returns true when the workspace contains a `.metro` directory.
func hasMetroDirectory(workspacePath: String) -> Bool {
    let metroDir = URL(fileURLWithPath: workspacePath).appendingPathComponent(".metro", isDirectory: true)
    var isDir: ObjCBool = false
    return FileManager.default.fileExists(atPath: metroDir.path, isDirectory: &isDir) && isDir.boolValue
}

/// Returns true when the workspace has `.metro/project.json`.
func hasMetroProjectSettings(workspacePath: String) -> Bool {
    let settingsURL = URL(fileURLWithPath: workspacePath)
        .appendingPathComponent(".metro", isDirectory: true)
